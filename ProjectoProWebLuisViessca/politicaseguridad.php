            <h2>Politica de seguridad</h2>
            <img src="Imagenes/imagenpolisegu.jpg" alt="">
            
            <p>ViescaElectronics establece como objetivo prioritario en materia de seguridad la
elaboración e implantación de una Política de Seguridad con el fin de:</p>
            <p>*Prevenir cualquier accidente industrial y/o laboral.</p>
            <p>*Proteger la salud de los trabajadores.</p>
            <p>La Seguridad y la Salud son los pilares fundamentales en los que se basa dicha
Política, por lo que la responsabilidad en la prevención de riesgos incumbe a todos
los componentes de la organización. En particular, los representantes de Dirección y
los distintos miembros de la línea jerárquica son los responsables de su adecuada
gestión y cada trabajador es responsable de su propia seguridad y de la de sus
compañeros de trabajo, así como de desarrollar sus tareas en el lugar de trabajo
procurando que no haya accidentes y no sufrir lesiones.</p>
            <p>La consecución de los objetivos de Seguridad y Salud tiene la misma prioridad que
otros aspectos relacionados con la calidad, productividad o eficiencia, de tal forma
que la gestión de la prevención se integra como un elemento más y de la misma
importancia que el resto de Políticas de la organización. El respaldo por parte de
Dirección a los planes de Seguridad Industrial y Salud Ocupacional es de amplio
compromiso y de obligado seguimiento en la asignación de recursos humanos,
económicos y técnicos, cumpliendo las exigencias legales en materia de Seguridad y
Salud en el trabajo.
Las normas y procedimientos de Seguridad Industrial y Salud Ocupacional
establecidos por ViescaElectronics. son de obligado cumplimiento por parte de todos los
empleados y subcontratistas que trabajen en las instalaciones de la empresa.
</p>
